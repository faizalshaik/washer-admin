<p>Privacy Policies are legally binding agreements you are <strong>required</strong> to post on your website if you’re collecting any sort of personal information from your site’s visitors or customers.</p>
<p>A Privacy Policy is an important legal document that lets users understand the various ways a website might be collecting personal information. The purpose of a Privacy Policy is to inform users of your data collection practices in order to protect the customer’s privacy.</p>
<p>Your Privacy Policy should disclose how the website/app collects information, how the information is used, whether or not it is shared with third parties and how it is protected and stored.</p>
<p>There are 3 main reasons for having a Privacy Policy: (1) you’re required by law, (2) you’re required by third party services, (3) you want to be transparent.</p>
<h2><span id="Required_by_Law">Required by Law</span></h2>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/required-by-law.jpg" alt="Required by Law" width="1000" height="300" class="alignnone size-full wp-image-203"></p>
<p>The most important reason Privacy Policies are useful is because you’re most likely required by the law to have one posted on your website. The applicable laws in your region or the region you’re conducting business in may require you to include and abide by certain clauses in your Privacy Policy.</p>
<p>For instance, in the United States, the California Online Privacy Protection Act (CalOPPA) <a href="https://oag.ca.gov/sites/all/files/agweb/pdfs/cybersecurity/making_your_privacy_practices_public.pdf">requires</a> websites that collect personal information from the residents of the state of California to include a statement in their Privacy Policy that discloses how you handle their information. Since there isn’t a way to filter out visitors from California, you’re likely required to comply with CalOPPA even if your website is ran from a location nowhere near California.</p>
<p><a href="https://www.forever21.com/us/shop/Info/PrivacyPolicy/privacy7">Forever 21’s</a> Privacy Policy agreement has a separate section on <em>California Residents</em> that explains the rights of the residents of California in compliance with CalOPPA.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/forever-21-privacy-policy-california-residents-clause.jpg" alt="Forever 21 Privacy Policy: California Residents Clause" width="971" height="198" class="alignnone size-full wp-image-204"></p>
<p>Similarly, Canada’s Personal Information Protection and Electronic Documents <a href="https://www.priv.gc.ca/en/privacy-topics/privacy-laws-in-canada/the-personal-information-protection-and-electronic-documents-act-pipeda/pipeda-compliance-help/">Act</a> (PIPEDA), the <a href="https://www.eugdpr.org/">EU’s</a> General Data Protection Regulation (GDPR) act, and <a href="https://www.oaic.gov.au/">Australia’s</a> Privacy Act of 1988 also require you to post a Privacy Policy and include certain clauses in it if you’re collecting any personal information from the residents of these regions.</p>
<h2><span id="Required_by_Third_Party_Services">Required by Third Party Services</span></h2>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/privacy-policies-required-by-third-parties.jpg" alt="Privacy Policies Required by Third Parties" width="1000" height="300" class="alignnone size-full wp-image-78"></p>
<p>Most of the third party services you use to improve your website’s user experience, monitor analytics or display advertisements also require you to post a Privacy Policy on your website. </p>
<p>According to their requirements, you should include clauses that disclose how you use these third party services, APIs, SDKs, plugins, etc.</p>
<p>Some of the most popular third party services that require you to post a Privacy Policy on your website include:</p>
<ul>
<li>Google Analytics</li>
<li>Google AdSense</li>
<li>Google AdWords</li>
<li>Amazon Affiliates</li>
<li>ClickBank</li>
<li>Twitter Lead Generation</li>
<li>Facebook Apps</li>
<li>Google Play Store</li>
<li>Apple’s App Store</li>
</ul>
<p>Analytics services work by placing cookies on your visitors’ devices and then collecting information about them when they visit your website, such as which device(s) they use, browsing activities, etc.</p>
<p>This is why third party services (like Google Analytics) require you to post a Privacy Policy that discloses your usage of their services and cookies.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/google-analytics-logo.jpg" alt="Logo of Google Analytics" width="300" height="97" class="alignnone size-full wp-image-28"></p>
<p><a href="https://www.google.com/analytics/terms/us.html">Google Analytics’</a> Terms of Service agreement states in its <em>Privacy</em> section that you must post a Privacy Policy on your website that discloses your use of cookies and your use of Google Analytics and how it collects and processes data.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/google-analytics-terms-service-privacy-policy-requirement-highlighted.jpg" alt="Google Analytics Terms of Service requires a Privacy Policy" width="1000" height="272" class="alignnone size-full wp-image-205"></p>
<p>Websites and web applications that use analytics services to improve the user experience they deliver must therefore abide by these rules.</p>
<p>If you’re a Facebook app developer, you’re required by <a href="https://developers.facebook.com/policy/#control">Facebook</a> to post a Privacy Policy on your website that’s easily accessible and discloses the information you collect and how you will use that information.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/facebook-app-developers-terms-page-give-people-control-clause.jpg" alt="Facebook Terms for App Developers: Give People Control Clause" width="980" height="663" class="alignnone size-full wp-image-206"></p>
<h2><span id="Transparency">Transparency</span></h2>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/transparency.jpg" alt="Transparency" width="1000" height="300" class="alignnone size-full wp-image-207"></p>
<p>As more and more people online are becoming aware of privacy laws, having a Privacy Policy displayed on your website that discloses how you gather and handle your visitors’ personal information is a great way to build trust and help your website users feel secure.</p>
<p>It’s a good practice to follow even if you’re not collecting any sort of personal information from your website’s visitors. This is because Privacy Policies are increasingly prevalent. If a visitor sees that you don’t have one published, she may be led to believe that you do, in fact, collect information from your visitors but aren’t disclosing it. It’s better to have one posted that states that you do not collect any information from your site’s visitors. This is especially true for blogs.</p>
<p><a href="https://duckduckgo.com/privacy">DuckDuckGo</a>, for example, has a Privacy Policy posted on its website that simply states that it doesn’t collect any personal information from visitors.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/duckduckgo-privacy-policy-does-not-collect-share-personal-information-screenshot.jpg" alt="DuckDuckGo: Screenshot of Privacy Policy page with highlighted excerpt - does not collect or share personal information" width="878" height="259" class="alignnone size-full wp-image-208"></p>
<h2><span id="Recommended_Clauses_for_Privacy_Policies">Recommended Clauses for Privacy Policies</span></h2>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/recommended-clauses-privacy-policies.jpg" alt="Recommended Clauses for Privacy Policies" width="1000" height="300" class="alignnone size-full wp-image-209"></p>
<p>A Privacy Policy should be organized in a way that helps the reader understand key categories of information. This is best done with well-structured and clearly written clauses, neatly identified with descriptive headlines.</p>
<p>The clauses you include in your Privacy Policy depend on a number of factors including the type of business you’re running and the applicable law. However, there are some clauses that just about every website that collects personal information from visitors includes in a Privacy Policy. </p>
<p>Let’s take a look at some examples of clauses that are useful to add in a Privacy Policy agreement:</p>
<h3><span id="Type_of_Information_You_Collect">Type of Information You Collect</span></h3>
<p>Most Privacy Policies start out by disclosing <strong>the type of information the business collects</strong> from its visitors or customers. It lets the end user know which type of personal information they can expect to provide, whether required or optional.</p>
<p>Let’s take a look at <a href="https://mailchimp.com/legal/privacy/">MailChimp’s</a> Privacy Policy agreement:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/mailchimp-privacy-policy-information-we-collect-clause-excerpt.jpg" alt="MailChimp Privacy Policy: Information We Collect clause excerpt" width="658" height="507" class="alignnone size-full wp-image-210"></p>
<p>MailChimp has an incredibly detailed <em>Your Information</em> section which explains what information it collects from users. It’s been divided into several sections – <em>Information you provide to us, Information we collect automatically, Information we collect from other sources, Information from the use of our mobile apps</em>. </p>
<p>It identifies the personal information you provide when you sign up with them and/or purchase their services such as name, address, email address, IP address, and credit card information.</p>
<p>Some web applications, like <a href="https://help.github.com/articles/github-privacy-statement/#what-information-github-collects-and-why">GitHub</a>, collect information from their site’s visitors in addition to their end users. </p>
<p>Here’s a look at how GitHub explains this in its Privacy Policy agreement:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/github-privacy-policy-information-website-browsers-users-accounts-clause.jpg" alt="GitHub Privacy Policy: Information from Website Browsers and users with accounts clauses" width="900" height="704" class="alignnone size-full wp-image-211"></p>
<p>GitHub explains that it collects personally-identifying information from website’s visitors and why it’s collected. The type of information collected from users who have accounts on GitHub is also disclosed.</p>
<h3><span id="How_the_Collected_Information_is_Processed_and_Shared">How the Collected Information is Processed and Shared</span></h3>
<p>You’re required to disclose <strong>how you process and share</strong> the personal information you collect from your site’s visitors. It should explain what you do with the information after you’ve collected it.</p>
<p>For example, in its Privacy Policy, <a href="https://www.logmeininc.com/legal/privacy">LogMeIn</a> explains the different ways it uses the personal information it collects:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/logmein-privacy-policy-how-we-use-information-we-collect-receive-clause.jpg" alt="LogMeIn Privacy Policy: How We Use the Information We Collect and Receive clause" width="1000" height="406" class="alignnone size-full wp-image-164"></p>
<p>LogMeIn also has a section in its Privacy Policy that explains how it shares visitors’ personal information with third parties:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/logmein-privacy-policy-page-information-sharing-third-party-clause.jpg" alt="LogMeIn Privacy Policy: Information Sharing clause discussing third party disclosure" width="1000" height="466" class="alignnone size-full wp-image-212"></p>
<h3><span id="Use_of_Cookies_and_Tracking">Use of Cookies and Tracking</span></h3>
<p>Websites that use cookies or other technologies to obtain personal information from their website’s visitors or customers include a cookies clause in their Privacy Policies. Generally, the cookies clause states that <strong>the website uses cookies</strong>, <strong>why</strong> it uses them, and <strong>how users can disable cookies</strong> on their devices.</p>
<p><a href="https://about.canva.com/privacy-policy/">Canva’s</a> Privacy Policy has a section on <em>Cookies information</em> that explains cookies usage.</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/canva-privacy-policy-cookies-information-clause.jpg" alt="Canva Privacy Policy: Cookies Information Clause" width="640" height="353" class="alignnone size-full wp-image-213"></p>
<p>It says that Canva uses cookies to improve your experience with their website by helping you log in faster and making their on-site navigation better. Cookies are also placed in order to track how you use the website. Canva discloses that its business partners also receive this information. Finally, it’s noted that if you disable cookies, some features of Canva might not work properly.</p>
<h3><span id="Changes_to_the_Privacy_Policy">Changes to the Privacy Policy</span></h3>
<p>You will likely have to change the content of your Privacy Policy at some time. For this reason, most Privacy Policies have a clause that states how they will inform users about updates and revisions to the agreement.</p>
<p>Let’s take a look at LogMeIn’s Privacy Policy again:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/logmein-privacy-policy-page-changes-statement-contact-clause.jpg" alt="LogMeIn Privacy Policy page: Changes to this Statement - Contact Us clause" width="919" height="229" class="alignnone size-full wp-image-214"></p>
<p>This clause states that the Policy may be updated and discloses how notifications of material changes will be given. It also says that if you continue to use the services after the update then you automatically agree to the revised Privacy Policy.</p>
<h2><span id="Examples_of_Websites_with_Privacy_Policies">Examples of Websites with Privacy Policies</span></h2>
<p>Regardless of whether you’re running a website, web app, mobile app or desktop app, if you’re collecting personal information from your end users then you’re required to post a Privacy Policy.</p>
<p>Most websites provide a link to their Privacy Policy in their homepage footer, main navigation, or an appropriate sub-menu. </p>
<p>Let’s take a look at the Privacy Policy agreements of some popular websites.</p>
<h3><span id="Reddit">Reddit</span></h3>
<p><a href="https://www.redditinc.com/policies/privacy-policy">Reddit</a> links to its Privacy Policy from the fine print in the footer of the homepage:</p>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/reddit-website-footer-links.jpg" alt="Reddit website footer with links" width="695" height="54" class="alignnone size-full wp-image-215"></p>
<p>The Privacy Policy itself follows the same format and theme as the rest of the website and includes anchor navigation links in the left sidebar. </p>
<p>It includes the following clauses:</p>
<ol>
<li>What We Collect</li>
<li>How We Use Information About You</li>
<li>How Information About You is Shared</li>
<li>Ads and Analytics Partners</li>
<li>Your Choices</li>
<li>Other Information</li>
<li>Contact Us</li>
</ol>
<p><img src="./Sample Privacy Policy Template - Free Privacy Policy_files/reddit-privacy-policy-screenshot-intro-table-contents.jpg" alt="Reddit: Screenshot of Privacy Policy page intro and table of contents" width="933" height="311" class="alignnone size-full wp-image-216"></p>
<h3><span id="Canva">Canva</span></h3>
